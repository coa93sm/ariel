export default class ArticleList {
  static articles = api.map((article) => new Article(article));

  static getTrending() {
    const trending = ArticleList.articles.filter((article) => article.trending);
    return ArticleList.getAllArticles(trending);
  }

  static getArticle(id) {
    return ArticleList.articles.find((article) => article.id == id);
  }

  static getAllArticles(articles = ArticleList.articles) {
    return articles.reduce(
      (sum, curr) => (sum += Article.displayArticle(curr)),
      ""
    );
  }

  static sortArticles(direction, criteria) {
    ArticleList.articles.sort((a, b) =>
      direction == "asc" ? a[criteria] - b[criteria] : b[criteria] - a[criteria]
    );
  }
}
